late D2ObjectBox db;

void main() async {
  db = await D2ObjectBox.createTest();

  Box<D2Event> box = db.store.box<D2Event>();
  Box<D2DataValue> dataValueBox = db.store.box<D2DataValue>();
  for (Map<String, dynamic> element in eventsPayload) {
    test("Initializes successfully from ${element["event"]}", () {
      D2Event event = D2Event.fromMap(db, element);
      expect(event.uid, element["event"]);
    });
    test("Saves successfully", () {
      D2Event event = D2Event.fromMap(db, element);
      int eventId = box.put(event);
      List<D2DataValue> dataValues = element["dataValues"]
          .map<D2DataValue>(
              (value) => D2DataValue.fromMap(db, value, element["event"]))
          .toList();
      dataValueBox.putMany(dataValues);
      expect(eventId.isNaN, false);
    });
    test("Saved successfully", () {
      D2Event? event =
          box.query(D2Event_.uid.equals(element["event"])).build().findFirst();
      expect(event != null, true);
      if (event == null) {
        return;
      }
      expect(event.uid, element["event"]);
      expect(event.synced, true);
      // expect(event.dataValues.isNotEmpty, true);
    });
    test("Data values saved successfully", () {
      D2Event? event =
          box.query(D2Event_.uid.equals(element["event"])).build().findFirst();
      if (event == null) {
        return;
      }
      List<D2DataValue> dataValues = dataValueBox
          .query(D2DataValue_.event.equals(event.id))
          .build()
          .find();
      expect(dataValues.isNotEmpty, true);
      expect(dataValues.length, element["dataValues"].length);
    });
  }
}
